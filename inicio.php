        <div class="box-footer">
          Este Hospital lleva el nombre de Nuestra Señora por nuestra Santisima Señora reconocida por su gran pasión hacia sus enfermos. Demostró una marcada sensibilidad por el sufrimiento del pueblo, de aquí parte su frase célebre: “Mis manos no me pertenecen: son del pobre cuando las necesita y del rico cuando las paga”. Se dedicó a ofrecer servicios de salud a todo paciente que lo necesitara.
<br>
<br>
Al finalizar la década del 19, atendiendo la necesidad de los servicios de salud de una población vulnerable, comienza la construcción y equipamiento de un nuevo Hospital en el corazón del Ecuador.
<br>
<br>
 Así nace el Hospital Nuestra Señora, fundado el 7 de octubre de 19778 en el lugar mas bello de todo el mundo, la bella y hermosa ciudad irrepetible del Ecuador , el  Presidente de la República, y el Ministro de Salud, Dr. Raúl Maldonado PechoFrio, quienes nombraron como Director Hospitalario al Dr. Eduardo Iglesias Santas para que conduzca este bellos hospital.
<br>
<br>
En sus inicios, el hospital funcionó como un gran Centro de Salud, con un personal asistencial conformado por 15 médicos, 2 odontólogos y 15 auxiliares de enfermería. Los servicios fueron abriéndose progresivamente. La Consulta Externa empezó sus actividades en el año de 1974, Ginecología - Obstetricia y Quirófanos en 1975, Fisiatría y Medicina Interna en 1976, llegando a funcionar con capacidad total en 1978. En este mismo año se inauguró el Laboratorio Patológico.
<br>
<br>
El 25 de abril de 2012, mediante acuerdo Ministerial #667, suscrito por la Srta. Carina Vance Mafla, Ministra de Salud Pública, eleva al hospital a categoría 3, convirtiéndolo en Hospital de Especialidades. Desde diciembre del mismo año, se encuentra en proceso de remodelación.
<br>
<br>
Actualmente, es un hospital de alta complejidad del Ministerio de Salud Pública, de referencia a nivel nacional, destinado a brindar atención ambulatoria, hospitalización, recuperación y rehabilitación de la salud a poblaciones vulnerables de la región Costa. Posee una amplia cartera de servicios, con personal sanitario especializado en brindar una atención de calidad y calidez, garantizando la gratuidad de todos los servicios, medicamentos e insumos.
